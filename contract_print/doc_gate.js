/* ===== ด่านร่วมของเอกสารทั้ง 5 ใบ — SSOT ของ "อะไรบล็อกได้บ้าง" =====
 * ที่มา (ตรวจเชิงลึก 20-07-2569 · ยืนยันด้วยการกดจริงบนเว็บ):
 *   เดิมแต่ละใบเขียนด่านของตัวเอง → ตรวจไม่เท่ากัน แล้วไม่มีอะไรเตือนว่าไม่เท่า
 *     · เพดานวงเงิน  ตรวจ 2 ใบ (หุ้นค้ำ/บุคคลค้ำ) — สัญญา ส.-งก.14 ที่ผูกพันที่สุด "ไม่ตรวจ"
 *       → พิมพ์สัญญาเกินเพดาน 4 เท่าได้ พร้อมข้อความ "✅ สำเร็จ" แต่หนังสือค้ำคู่กันถูกบล็อก
 *         = ได้แฟ้มที่มีสัญญาแต่ไม่มีหลักประกัน
 *     · ช่องบังคับ    ตำบลตรวจใบเดียว · วัตถุประสงค์ตรวจ 2 ใบ
 *     · ความจุตาราง  ส.-งก.14 บล็อกที่ 10 งวด · ส.-งก.13 ปล่อยผ่านแล้วงวดที่ 11+ หายเงียบ
 *
 * มติผู้จัดการ 20-07-2569: ให้ทั้ง 5 ใบ "บล็อกเหมือนกัน" ผ่านด่านร่วมตัวเดียว
 *
 * 🔒 กฎการแก้ต่อจากนี้: เพิ่ม/แก้เงื่อนไขบล็อก ให้แก้ที่ไฟล์นี้ที่เดียว
 *    ห้ามเขียนด่านซ้ำในตัวสร้างเอกสาร — สองสูตรที่ตั้งใจให้เหมือนกันจะเพี้ยนจากกันเงียบ ๆ
 *    (แบบแผนเดียวกับที่ ceilingFor เตือนไว้แล้วใน loan_calculator.html แต่ยังเกิดซ้ำ)
 */
(function () {
  'use strict';

  var V = function (id) {
    var el = document.getElementById(id);
    return el ? String(el.value || '').trim() : '';
  };

  /* ---- ช่องข้อมูลผู้กู้: id → ป้ายที่แสดงตอนแจ้งว่าขาด ---- */
  var FIELDS = {
    name:   ['ct_name',   'ชื่อ-สกุล'],
    reg:    ['ct_reg',    'เลขทะเบียน'],
    group:  ['ct_group',  'กลุ่มที่'],
    house:  ['ct_house',  'บ้านเลขที่'],
    moo:    ['ct_moo',    'หมู่ที่'],
    tambon: ['ct_tambon', 'ตำบล']
    // purpose ตรวจแยก — มีเคส __OTHER__ ที่ต้องดูช่องพิมพ์เองด้วย
  };

  /* ---- แต่ละใบบังคับช่องไหน = "ช่องที่ใบนั้นพิมพ์จริง" ----
   * เกณฑ์: ถ้าเอกสารมีที่ให้ลงค่านั้น ต้องมีค่า · ถ้าไม่มีที่ลง ไม่ต้องบังคับ
   * (บังคับเกินที่พิมพ์ = กวนเจ้าหน้าที่โดยไม่ได้อะไร)
   *   contract  ส.-งก.14 พิมพ์ที่อยู่เต็ม + วัตถุประสงค์
   *   sngk13    คำขอกู้ พิมพ์ที่อยู่เต็ม + วัตถุประสงค์
   *   share     หนังสือหุ้นค้ำ พิมพ์ที่อยู่เต็ม (ไม่มีช่องวัตถุประสงค์)
   *   guarantee หนังสือค้ำ พิมพ์ชื่อผู้กู้ + วัตถุประสงค์ (ที่อยู่ที่พิมพ์คือของผู้ค้ำ ตรวจแยกอีกชุด)
   *   janong    ความจำนง พิมพ์ชื่อ/ทะเบียน/กลุ่ม + วัตถุประสงค์ (ไม่มีช่องที่อยู่) */
  var NEED = {
    contract:  ['name', 'reg', 'group', 'house', 'moo', 'tambon', 'purpose'],
    sngk13:    ['name', 'reg', 'group', 'house', 'moo', 'tambon', 'purpose'],
    share:     ['name', 'reg', 'group', 'house', 'moo', 'tambon'],
    guarantee: ['name', 'purpose'],
    janong:    ['name', 'reg', 'group', 'purpose']
  };

  /* ---- ช่องบังคับของผู้ค้ำแต่ละคน (หนังสือค้ำประกัน) ----
   * gTambon อยู่ในนี้ด้วย — เดิมไม่ตรวจ เพราะดรอปดาวน์ไม่มี option ว่าง
   * จึงไม่เคยว่าง แล้วพิมพ์ "นครชัยศรี" ให้เองทั้งที่ไม่มีใครเลือก (บั๊ก 20-07) */
  var GUAR_FIELDS = [
    ['name',   'ชื่อ-สกุล'],
    ['reg',    'เลขทะเบียน'],
    ['group',  'กลุ่มที่'],
    ['house',  'บ้านเลขที่'],
    ['moo',    'หมู่ที่'],
    ['tambon', 'ตำบล']
  ];

  function borrower(doc) {
    var need = NEED[doc] || [], miss = [];
    need.forEach(function (k) {
      if (k === 'purpose') {
        var pv = V('ct_purpose');
        if (!pv || (pv === '__OTHER__' && !V('ct_purpose_other'))) miss.push('วัตถุประสงค์');
        return;
      }
      var f = FIELDS[k];
      if (f && !V(f[0])) miss.push(f[1]);
    });
    return miss;   // [] = ครบ
  }

  function guarantor(i) {
    var miss = [];
    GUAR_FIELDS.forEach(function (f) {
      if (!V('g' + i + '_' + f[0])) miss.push(f[1]);
    });
    if (V('g' + i + '_id').replace(/\D/g, '').length !== 13) miss.push('เลขบัตร 13 หลัก');
    return miss;
  }

  /* ---- เพดานวงเงิน (ระเบียบเงินกู้ ข้อ 8) ----
   * คืน null = ผ่าน · คืนสตริง = ข้อความบล็อก
   * 🔑 ceilingFor คืน null เมื่อระเบียบไม่ได้ผูกเพดานกับตัวเลขที่เว็บรู้ (จำนอง/เงินฝาก)
   *    ต้องเช็ก null "ก่อน" เปรียบเทียบเสมอ — ถ้าปล่อยไปเทียบ null จะกลายเป็น 0 แล้วบล็อกทุกวงเงิน
   *    (กับดักนี้ยังไม่เคยระเบิด เพราะผู้เรียกเดิมกันไว้ก่อนทุกจุด — ด่านนี้กันให้ถาวร) */
  function ceiling(P) {
    /* 🔒 fail-closed เหมือน capacity() ด้านล่าง — อ่านสูตร/หลักประกัน/หุ้นไม่ได้ = ห้ามปล่อยผ่าน
     * เดิม 3 บรรทัดนี้เป็น fail-open: ไม่มี ceilingFor → คืน null (ผ่าน) และเดาว่าหลักประกัน
     * เป็น 'person' ถ้าอ่านไม่ได้ ถ้า loan_core.js โหลดไม่ทัน เพดานจะหายเงียบพร้อมกันทั้ง 5 ใบ
     * โดยไม่มีอะไรบอก — สวนทางกับหลักที่ไฟล์นี้เขียนไว้เอง (ตรวจเชิงลึก 21-07-2569)
     * และ "เดาหลักประกัน" อันตรายเป็นพิเศษ เพราะเพดานของแต่ละชนิดต่างกันคนละเรื่อง */
    var missing = [];
    if (typeof ceilingFor  !== 'function') missing.push('สูตรเพดาน (loan_core.js)');
    if (typeof getSecurity !== 'function') missing.push('ชนิดหลักประกัน');
    if (typeof getShares   !== 'function') missing.push('จำนวนหุ้น');
    if (missing.length) {
      return 'ระบบผิดพลาด: อ่าน ' + missing.join(' · ') + ' ไม่ได้ — แจ้งผู้พัฒนา'
           + '<br><span style="font-weight:400">ไม่พิมพ์ให้ เพราะตรวจเพดานวงเงินตามระเบียบ ข้อ 8 ไม่ได้</span>';
    }
    var sec    = getSecurity();
    var shares = getShares();
    var cap    = ceilingFor(sec, shares);
    if (cap === null || cap === undefined) return null;    // คณะกรรมการเป็นผู้พิจารณา ไม่ใช่เว็บ
    /* จำนวนหุ้นบังคับเฉพาะเส้นทางที่เพดานผูกกับหุ้นโดยตรง = หุ้นค้ำ (60% ของมูลค่าหุ้น)
     * บุคคลค้ำ: ปกติไม่ต้องดูหุ้น — ได้พื้น 50,000 ตาม ข้อ 8(2) ก. อยู่แล้ว
     *   หุ้นมีผลแค่ "ขยาย" เพดานเป็น 2 เท่าของหุ้น (สูงสุด 80,000) ตาม ข้อ 8(2) ข.
     *   → ไม่กรอกหุ้น = ใช้พื้น 50,000 · อยากได้เกินนั้นค่อยกรอกหุ้นเพื่อขยาย
     * (ผู้จัดการ 20-07: "ปกติจะไม่ดูจำนวนหุ้น เว้นแต่จะให้หุ้นค้ำร่วมด้วย หรือ 60% ของหุ้น") */
    if (sec === 'share' && !shares) {
      return 'ยังไม่ได้กรอก “จำนวนหุ้นที่ถืออยู่” — หุ้นค้ำประกันคิดเพดานจาก 60% ของมูลค่าหุ้น (ระเบียบ ข้อ 8 วรรคแรก)';
    }
    if (P > cap) {
      var rule = (sec === 'person') ? 'ข้อ 8(2)' : 'ข้อ 8 วรรคแรก';
      return 'วงเงิน ' + fmt0(P) + ' บาท เกินเพดาน ' + fmt0(cap) + ' บาท (ระเบียบเงินกู้ ' + rule + ')'
           + '<br><span style="font-weight:400">→ ลดวงเงิน หรือใช้จำนองอสังหาฯ [8(1)] / เงินฝาก [8(3)] แทนหรือร่วม</span>';
    }
    return null;
  }

  /* ---- เพดานรวมทุกสัญญา 1,000,000 บาท (ระเบียบ 101 ข้อ 6 วรรคท้าย) ----
   * ต่างจาก ceiling() ที่ดูวงเงินใบเดียวเทียบหลักประกัน — ตัวนี้ดู "ยอดรวม"
   * ของเงินกู้ระยะสั้น+ปานกลางทุกรายการที่สมาชิกมีอยู่ในเวลาเดียวกัน
   * หนี้เดิมอ่านจากตารางใน ส.-งก.13 (ช่องอยู่ใน DOM เสมอ แม้ยังไม่เปิดแผง)
   * fail-closed แบบเดียวกับด่านอื่น: อ่านสูตรไม่ได้ = ไม่ปล่อยผ่าน */
  function totalCeiling(P) {
    if (typeof loanTotalCounted !== 'function' || typeof TOTAL_CEILING !== 'number') {
      return 'ระบบผิดพลาด: อ่านสูตรเพดานรวม (loan_core.js) ไม่ได้ — แจ้งผู้พัฒนา'
           + '<br><span style="font-weight:400">ไม่พิมพ์ให้ เพราะตรวจเพดานรวม 1,000,000 บาท ตามระเบียบ ข้อ 6 ไม่ได้</span>';
    }
    var debts = [], i = 1;
    while (document.getElementById('s13_debt' + i + '_type')) {
      debts.push({ type: V('s13_debt' + i + '_type'), remain: V('s13_debt' + i + '_remain') });
      i++;
    }
    var r = loanTotalCounted(P, debts);
    if (r.sum <= TOTAL_CEILING) return null;

    var detail = r.debtSum > 0
      ? 'วงเงินใหม่ ' + fmt0(r.newLoan) + ' + หนี้เดิมที่กรอกไว้ ' + fmt0(r.debtSum)
        + ' (' + r.counted.length + ' รายการ) = ' + fmt0(r.sum) + ' บาท'
      : 'วงเงินใหม่ ' + fmt0(r.sum) + ' บาท';
    var note = r.skipped.length
      ? '<br><span style="font-weight:400">ไม่นับหนี้ประเภท ' + r.skipped.join(' · ')
        + ' เพราะระเบียบข้อนี้ครอบเฉพาะระยะสั้นและระยะปานกลาง</span>'
      : '';
    return 'ยอดรวมเกินเพดาน 1,000,000 บาท (ระเบียบเงินกู้ ข้อ 6 วรรคท้าย)'
         + '<br><span style="font-weight:400">' + detail + '</span>' + note
         + '<br><span style="font-weight:400">→ ลดวงเงิน หรือชำระหนี้เดิมบางส่วนก่อน</span>';
  }

  /* ---- ความจุตารางงวดในแบบฟอร์ม ----
   * นับช่องจริงจาก fieldmap ไม่ฝังเลข 10 ไว้ — เพิ่มแถวในฟอร์มแล้วด่านขยับตามเอง */
  function slots(doc) {
    if (doc === 'contract') {
      var t = window.FIELD_MAP && window.FIELD_MAP.table;
      return t ? (t.segments || []).reduce(function (s, x) { return s + (x.cap || 0); }, 0) : 0;
    }
    if (doc === 'sngk13') {
      var M = window.SNGK13_MAP || {}, n = 0;
      while (M['rep' + (n + 1) + '_amt']) n++;
      return n;
    }
    return 0;
  }

  function capacity(doc, prinCount) {
    var cap = slots(doc);
    /* 🔒 fail-closed: นับความจุไม่ได้ = ไม่รู้ว่าตารางรับได้กี่งวด → ห้ามปล่อยผ่าน
     *    เดิมเขียน `if(!cap) return null` = ถ้า fieldmap โหลดไม่ทัน/โครงสร้างเปลี่ยน
     *    ด่านจะเงียบแล้วปล่อยผ่าน ทั้งที่ทั้งไฟล์นี้ตั้งใจให้บล็อกเมื่อไม่แน่ใจ
     *    (ผู้ตรวจรับจับได้ 20-07 — ด่านที่ fail แบบเงียบ = ด่านที่ไม่มีอยู่จริง) */
    if (!cap) {
      return 'ระบบผิดพลาด: อ่านความจุตารางงวดจากแบบฟอร์มไม่ได้ (' + doc + ') — แจ้งผู้พัฒนา'
           + '<br><span style="font-weight:400">ไม่พิมพ์ให้ เพราะถ้าตารางรับไม่พอ งวดท้ายจะหายเงียบ</span>';
    }
    if (prinCount <= cap) return null;
    return 'งวดชำระ ' + prinCount + ' งวด เกินความจุตารางในแบบฟอร์ม (' + cap + ' งวด)'
         + '<br><span style="font-weight:400">งวดท้ายจะหายจากเอกสารโดยไม่มีการเตือน '
         + 'แต่ยอดรวมยังเป็นของเต็ม = เอกสารขัดแย้งในตัวเอง — ลดจำนวนปี หรือใช้แบบฟอร์มที่ตารางยาวกว่า</span>';
  }

  /* ---- กันบั๊ก "ช่องว่างเงียบ" ----
   * ตรวจเฉพาะ key ที่ไม่ถูกสร้างเลย (undefined = บั๊กโปรแกรม)
   * ค่าว่าง '' = คนตั้งใจเว้น → ผ่าน
   * ⚠️ ใช้ได้เฉพาะใบที่ฝั่งผลิตสร้าง "ครบทุก key" (contract/share/guarantee)
   *    ส.-งก.13 กับ ความจำนง มี key ของแถวตารางส่วนเกินที่ตั้งใจปล่อยว่าง → ใช้ด่านนี้ไม่ได้ */
  function missKeys(map, data) {
    if (!map || !data) return [];
    return Object.keys(map).filter(function (k) {
      var spec = map[k];
      // นับเฉพาะ "ช่องจริง" = object ที่มี page — ตัด baseDY (ตัวเลข) และ table (ตารางงวด ใช้ data.rows คนละทาง)
      if (!spec || typeof spec !== 'object' || !('page' in spec)) return false;
      return data[k] === undefined;
    });
  }

  window.DocGate = {
    borrower: borrower,
    guarantor: guarantor,
    ceiling: ceiling,
    totalCeiling: totalCeiling,
    capacity: capacity,
    slots: slots,
    missKeys: missKeys,
    _NEED: NEED          // เปิดให้ตรวจสอบ/เทสต์ ไม่ใช่ให้แก้ตอนรัน
  };
})();
